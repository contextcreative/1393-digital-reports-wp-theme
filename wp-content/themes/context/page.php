										<p>The Patient Ombudsman’s authority is set out in the Excellent Care for All Act, 2010. Our role is to receive, respond to and help resolve complaints from current or former patients or their caregivers about their care or experiences with health sector organizations. Our jurisdiction currently includes public hospitals, long-term care homes and Local Health Integration Network (LHIN) home and community care services (formerly Community Care Access Centres (CCACs)). As an impartial office of last resort, we can help when patients and caregivers have not been able to resolve their complaint directly through the internal complaints process with their health service organization. There is no charge for anyone to file a complaint with our office. </p>
